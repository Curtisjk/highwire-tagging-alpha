		<h1 id="welcome" style="padding-top: 100px; margin-top: -100px;">Welcome</h1>
		<p>Thanks for dropping by and considering helping out with this piece of academic research into ways in which crowdsourced news media might be improved.</p>
		<p>We are hoping you will view the short (less than 2 minutes) video on the next page and provide some annotation throughout (by tagging) as to what is going on - there are some nice simple instructions on the next page. By the way, tagging just means typing short one or two word comments into a text box below the video, whilst watching the video. You can tag things as many times as you like!</p>
		<p>Just to make it clear &amp; simple we DON'T automatically collect ANY personal information but we do use a simple cookie to track which video you are watching and offer up other videos to tag if you're willing (we delete it once you're done). There is an option to leave an email address at the end AND if you're feeling super helpful links to help you recruit others to the study.</p>
		<p>The only things we do automatically collect are a random unique user number, your response to the question alongside the video and your tags. We may well hang on to the data for use in later academic work but will NOT pass or sell this data on to anyone else.</p>
		<p>Participation is voluntary and you can, of course, drop out at any point (even part way through a video). Just hit the "I've finished" button and we part ways.</p> 
		<p>Thanks for your help, don't forget to read the formals below and tick that consent box.</p>
		<p><i>Barney Craggs &amp; Myles Scott</i></p>
		
